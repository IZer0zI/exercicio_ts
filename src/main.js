"use strict";
// Função de multiplicação
function multiplicar(a, b) {
    return a * b;
}
// Função de saudação
function saudar(nome) {
    return "Olá " + nome;
}
// Exemplos de uso
const resultadoMultiplicacao = multiplicar(3, 4);
const saudacao = saudar("Alice");
console.log(resultadoMultiplicacao);
console.log(saudacao);
